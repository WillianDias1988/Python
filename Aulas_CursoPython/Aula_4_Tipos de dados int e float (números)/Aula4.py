# Tipos int e float
# int -> numero inteiro
# o tipo int representa qualquer numero inteiro
# positivo ou negativo. int sem sinal ou com sinal

print(11)  # int
print(-11)  # int
print(0)  # int

# float -> numero com ponto flutuante
# O tipo float representa qualquer numero
# posito ou negativo com ponto flutuante

print(11.0)  # float
print(-11.0)  # float
print(0.0)  # float

# função type mostra o tipo que o Python inferiu ao valor

print(type(1))
print(type(1.1))
print(type('Willian'))
print(type(True))
